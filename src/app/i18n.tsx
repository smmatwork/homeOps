const I18nContext = createContext<I18nContextValue | null>(null);

function normalizeLang(input: string): UiLanguage {
  if (input === "hi") return "hi";
  if (input === "kn") return "kn";
  return "en";
}

export function UiLanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<UiLanguage>(() => {
    try {
      return normalizeLang(localStorage.getItem(STORAGE_KEY) ?? "en");
    } catch {
      return "en";
    }
  });

  const setLang = useCallback((next: UiLanguage) => {
    setLangState(next);
    try {
      localStorage.setItem(STORAGE_KEY, next);
    } catch {
      // ignore
    }
  }, []);

  useEffect(() => {
    try {
      localStorage.setItem(STORAGE_KEY, lang);
    } catch {
      // ignore
    }
  }, [lang]);

  const t = useCallback(
    (key: string) => {
      const table = TRANSLATIONS[lang] ?? TRANSLATIONS.en;
      if (key in table) return table[key];
      const fallback = TRANSLATIONS.en;
      if (key in fallback) return fallback[key];
      return key;
    },
    [lang],
  );

  const value = useMemo<I18nContextValue>(() => ({ lang, setLang, t }), [lang, setLang, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (ctx) return ctx;
  return {
    lang: "en",
    setLang: () => {
      return;
    },
    t: (key: string) => {
      const fallback = TRANSLATIONS.en;
      if (key in fallback) return fallback[key];
      return key;
    },
  };
}
